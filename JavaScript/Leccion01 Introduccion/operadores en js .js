z = a - b;
console.log("resultado de la resta : " +z );

z = a * b ;
console.log("El resultado de la mult:" + z);

z = a / b ;
console.log("resultado de la div :" + z);

z = a % b;
console.log("el valor del modulo o residuo :" +z );

z = a ** b;
console.log("resultado operador exponente: "+z);

        //  INCREMENTO
    //  pre incremento (el operador ++ antes de la variable) primero se incrementa el valor de la variable antes de ser utilizado
z = ++a;
console.log(a);
console.log(z);

    //  post incremento (el operador ++ despues de la variable) primero se utiliza el valor original y posteriormente se incrementa.

z = b++;
console.log(b);
console.log(z);


         //  DECRECREMENTO
    //  pre decrecremento
z = --a;
console.log(z);
console.log(a);

    // post decremento
z = b--;
console.log(b);
console.log(z);

